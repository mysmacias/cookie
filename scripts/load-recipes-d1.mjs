#!/usr/bin/env node
/**
 * Generate D1 seed SQL from data/recipes.json for two targets:
 *   - catalog: the global `recipes` table (migration 0005)
 *   - user:    a specific user's `user_recipes` library (resolved by email,
 *              so no user PII needs to be read back)
 *
 * This script only WRITES SQL files (idempotent INSERT OR REPLACE). Apply them
 * with wrangler, e.g.:
 *   npx wrangler d1 execute cookie-db --local  --file=data/seed/catalog.sql
 *   npx wrangler d1 execute cookie-db --remote --file=data/seed/catalog.sql
 *
 * Usage:
 *   node scripts/load-recipes-d1.mjs --email=you@example.com
 *   node scripts/load-recipes-d1.mjs --targets=catalog        # catalog only
 */

import { mkdir, writeFile, readFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const DB_PATH = path.join(ROOT, 'data', 'recipes.json');
const SEED_DIR = path.join(ROOT, 'data', 'seed');

function parseArgs(argv) {
  // One INSERT per row: large recipe JSON makes multi-row statements exceed
  // D1's max statement size (SQLITE_TOOBIG). wrangler batches the statements.
  const opts = { email: 'mysmacias@gmail.com', targets: ['catalog', 'user'], chunk: 1 };
  for (const arg of argv) {
    if (arg.startsWith('--email=')) opts.email = arg.slice(8);
    else if (arg.startsWith('--targets=')) opts.targets = arg.slice(10).split(',').map(s => s.trim()).filter(Boolean);
    else if (arg.startsWith('--chunk=')) opts.chunk = parseInt(arg.slice(8), 10) || opts.chunk;
  }
  return opts;
}

// SQLite string literal: double single quotes; backslashes are literal.
const q = s => `'${String(s).replace(/'/g, "''")}'`;

function toRecipe(rec) {
  // Map a scraped record to the app's Recipe shape (src/types.ts) with a stable
  // scrape_ id so the client treats it as an imported recipe (keeps its image).
  const id = rec.id.startsWith('scrape_') ? rec.id : `scrape_${rec.id}`;
  const recipe = {
    id,
    title: rec.title,
    description: rec.description,
    image: rec.image,
    difficulty: rec.difficulty,
    time: rec.time,
    prepTime: rec.prepTime,
    ...(rec.bakeTime ? { bakeTime: rec.bakeTime } : {}),
    ...(rec.yields ? { yields: rec.yields } : {}),
    category: rec.category,
    tags: rec.tags ?? [],
    ingredients: rec.ingredients ?? [],
    steps: rec.steps ?? [],
    ...(rec.sourceUrl ? { sourceUrl: rec.sourceUrl } : {}),
    addedAt: rec.scrapedAt ?? Date.now(),
  };
  return { id, recipe };
}

function domainOf(url) {
  try {
    return new URL(url).hostname.replace(/^www\./, '');
  } catch {
    return '';
  }
}

function buildCatalogSql(records, chunkSize) {
  // No explicit BEGIN/COMMIT: remote D1 rejects them (it wraps batches itself).
  const lines = ['-- Global recipe catalog seed (idempotent). Generated by scripts/load-recipes-d1.mjs'];
  for (let i = 0; i < records.length; i += chunkSize) {
    const chunk = records.slice(i, i + chunkSize);
    const values = chunk.map(rec => {
      const { id, recipe } = toRecipe(rec);
      const data = JSON.stringify(recipe);
      return `(${q(id)}, ${q(data)}, ${q(recipe.title)}, ${q(recipe.sourceUrl ?? '')}, ${q(domainOf(recipe.sourceUrl ?? ''))}, ${q(recipe.image ?? '')}, ${recipe.addedAt})`;
    });
    lines.push(
      'INSERT OR REPLACE INTO recipes (id, data, title, source_url, source_domain, image, created_at) VALUES\n' +
        values.join(',\n') +
        ';',
    );
  }
  return lines.join('\n') + '\n';
}

function buildUserSql(records, email) {
  // user_id is resolved from the users table by email via a subquery, so the
  // generated SQL is identical for local and remote and never needs PII read back.
  const lines = [
    `-- user_recipes seed for ${email} (idempotent). Generated by scripts/load-recipes-d1.mjs`,
  ];
  for (const rec of records) {
    const { id, recipe } = toRecipe(rec);
    const data = JSON.stringify(recipe);
    lines.push(
      `INSERT OR REPLACE INTO user_recipes (id, user_id, data, updated_at) ` +
        `SELECT ${q(id)}, id, ${q(data)}, ${recipe.addedAt} FROM users WHERE email = ${q(email)};`,
    );
  }
  return lines.join('\n') + '\n';
}

async function main() {
  const opts = parseArgs(process.argv.slice(2));
  const records = JSON.parse(await readFile(DB_PATH, 'utf8'));
  await mkdir(SEED_DIR, { recursive: true });

  console.log(`Loaded ${records.length} recipes from data/recipes.json`);
  const written = [];

  if (opts.targets.includes('catalog')) {
    const sql = buildCatalogSql(records, opts.chunk);
    const file = path.join(SEED_DIR, 'catalog.sql');
    await writeFile(file, sql, 'utf8');
    written.push(['catalog', file, sql.length]);
  }
  if (opts.targets.includes('user')) {
    const sql = buildUserSql(records, opts.email);
    const file = path.join(SEED_DIR, 'user-recipes.sql');
    await writeFile(file, sql, 'utf8');
    written.push(['user', file, sql.length]);
  }

  for (const [target, file, size] of written) {
    console.log(`  ${target}: ${path.relative(ROOT, file)} (${(size / 1048576).toFixed(2)} MB)`);
  }
  console.log('\nApply with:');
  for (const [, file] of written) {
    const rel = path.relative(ROOT, file);
    console.log(`  npx wrangler d1 execute cookie-db --local  --file=${rel}`);
    console.log(`  npx wrangler d1 execute cookie-db --remote --file=${rel}`);
  }
}

main().catch(e => {
  console.error(e);
  process.exit(1);
});
