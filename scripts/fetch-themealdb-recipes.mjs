#!/usr/bin/env node

import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';

const DEFAULTS = {
  areas: 'Greek,Italian,Spanish,Moroccan,Egyptian,Turkish',
  limit: 24,
  delayMs: 200,
  output: 'src/data/mealDbSeeds.ts',
};

function parseArgs(argv) {
  const args = { ...DEFAULTS };
  for (const arg of argv) {
    if (!arg.startsWith('--')) continue;
    const [rawKey, rawValue = ''] = arg.slice(2).split('=');
    const key = rawKey.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
    if (key === 'limit' || key === 'delayMs') {
      args[key] = Number.parseInt(rawValue, 10);
    } else if (key in args) {
      args[key] = rawValue;
    }
  }
  return args;
}

function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

async function mealDbFetch(key, route, params) {
  const url = new URL(`https://www.themealdb.com/api/json/v1/${key}/${route}`);
  for (const [name, value] of Object.entries(params ?? {})) {
    if (value !== undefined && value !== null && value !== '') {
      url.searchParams.set(name, String(value));
    }
  }

  const res = await fetch(url);
  if (!res.ok) {
    throw new Error(`TheMealDB request failed (${res.status})`);
  }

  return res.json();
}

function firstSentence(text) {
  const cleaned = String(text ?? '').replace(/\s+/g, ' ').trim();
  if (!cleaned) return '';
  const match = /^(.+?[.!?])\s/.exec(cleaned);
  return match?.[1] ?? cleaned.slice(0, 220);
}

function parseInstructions(instructions) {
  return String(instructions ?? '')
    .split(/\r?\n+|\.\s+(?=\d+\.|\p{Lu})/u)
    .map(step => step.replace(/^\d+\.\s*/, '').trim())
    .filter(Boolean)
    .map((description, index) => ({
      title: `Step ${index + 1}`,
      description: description.endsWith('.') ? description : `${description}.`,
    }));
}

function extractIngredients(meal) {
  const ingredients = [];
  for (let i = 1; i <= 20; i += 1) {
    const name = String(meal[`strIngredient${i}`] ?? '').trim();
    const amount = String(meal[`strMeasure${i}`] ?? '').trim();
    if (!name) continue;
    ingredients.push({ name, amount: amount || 'to taste' });
  }
  return ingredients;
}

function buildTags(meal) {
  const tags = new Set(['mealdb']);
  if (meal.strArea) tags.add(String(meal.strArea).toLowerCase());
  if (meal.strCategory) tags.add(String(meal.strCategory).toLowerCase());
  for (const tag of String(meal.strTags ?? '').split(',')) {
    const value = tag.trim().toLowerCase();
    if (value) tags.add(value);
  }

  const mediterraneanAreas = new Set(['greek', 'italian', 'spanish', 'moroccan', 'egyptian', 'turkish']);
  if (mediterraneanAreas.has(String(meal.strArea ?? '').toLowerCase())) {
    tags.add('mediterranean');
  }

  return [...tags].sort();
}

function mapMealDbToCookie(meal) {
  const steps = parseInstructions(meal.strInstructions);
  return {
    id: `mealdb_${meal.idMeal}`,
    title: meal.strMeal,
    description: firstSentence(meal.strInstructions) || `${meal.strArea} ${meal.strCategory} recipe from TheMealDB.`,
    image: meal.strMealThumb || '',
    difficulty: 'Medium',
    time: '—',
    prepTime: '—',
    category: meal.strCategory || 'Imported',
    tags: buildTags(meal),
    ingredients: extractIngredients(meal),
    steps: steps.length > 0 ? steps : [{ title: 'Step 1', description: 'Prepare according to the source recipe.' }],
    chefNote: meal.strSource ? `Source: ${meal.strSource}` : undefined,
    addedAt: Date.now(),
  };
}

async function collectMeals(args) {
  const key = process.env.MEALDB_API_KEY || '1';
  const ids = new Map();
  const areas = String(args.areas).split(',').map(area => area.trim()).filter(Boolean);

  for (const area of areas) {
    await sleep(args.delayMs);
    const result = await mealDbFetch(key, 'filter.php', { a: area });
    for (const meal of result.meals ?? []) {
      if (meal?.idMeal) ids.set(meal.idMeal, meal);
      if (ids.size >= args.limit) break;
    }
    if (ids.size >= args.limit) break;
  }

  const meals = [];
  for (const id of [...ids.keys()].slice(0, args.limit)) {
    await sleep(args.delayMs);
    const result = await mealDbFetch(key, 'lookup.php', { i: id });
    const meal = result.meals?.[0] ?? ids.get(id);
    if (meal) meals.push(mapMealDbToCookie(meal));
  }

  return meals;
}

function renderSeedFile(meals, args) {
  const generatedAt = new Date().toISOString();
  const json = JSON.stringify(meals, null, 2);
  return `import type { Recipe } from '../types';

// Generated by scripts/fetch-themealdb-recipes.mjs
// Source: themealdb.com, areas=${args.areas}, generatedAt=${generatedAt}
export const MEALDB_SEED_RECIPES = ${json} satisfies Recipe[];
`;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  if (!Number.isInteger(args.limit) || args.limit <= 0) {
    throw new Error('--limit must be a positive integer.');
  }

  const meals = await collectMeals(args);
  const outputPath = path.resolve(process.cwd(), args.output);
  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, renderSeedFile(meals, args), 'utf8');
  console.log(`Wrote ${meals.length} TheMealDB recipes to ${args.output}`);
}

main().catch(error => {
  console.error(error.message);
  process.exit(1);
});
