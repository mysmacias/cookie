#!/usr/bin/env node
/**
 * Scrape real recipes (with pictures) from the web and regenerate the app's
 * DEFAULT recipe library: src/data/bundledRecipes.ts.
 *
 * It fetches each page, extracts the embedded schema.org/Recipe JSON-LD,
 * maps it to the Cookie `Recipe` shape, downloads the hero image into
 * public/recipe-images/, and writes a fresh bundledRecipes.ts. The scraped
 * recipes REPLACE the previous hand-authored defaults.
 *
 * Usage:
 *   node scripts/scrape-default-recipes.mjs              # uses the curated list below
 *   node scripts/scrape-default-recipes.mjs --urls=urls.txt   # one URL per line
 *   node scripts/scrape-default-recipes.mjs https://site/recipe ...
 *   node scripts/scrape-default-recipes.mjs --no-images  # keep remote image URLs
 *
 * Parsing/mapping lives in scripts/lib/scrape-core.mjs (shared with the bulk
 * crawler) and mirrors functions/lib/scraper.ts.
 */

import { mkdir, writeFile, readFile, rm } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { FETCH_HEADERS, scrapeRecipe, sleep } from './lib/scrape-core.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');

// ---------------------------------------------------------------------------
// Curated default library: diverse, reliable sources that publish schema.org
// Recipe JSON-LD. Edit this list to change the bundled defaults.
// ---------------------------------------------------------------------------
