  'https://www.recipetineats.com/chocolate-chip-cookies/',
  'https://sallysbakingaddiction.com/best-banana-bread/',
  'https://www.bbcgoodfood.com/recipes/best-ever-chocolate-brownies-recipe',
  'https://www.bbcgoodfood.com/recipes/classic-victoria-sandwich-recipe',
  'https://sallysbakingaddiction.com/apple-pie/',
  'https://sallysbakingaddiction.com/homemade-bagels/',
  'https://sallysbakingaddiction.com/scones-recipe/',
  'https://www.recipetineats.com/pizza-dough-recipe/',
  // Breakfast
  'https://www.budgetbytes.com/homemade-pancakes/',
  'https://www.loveandlemons.com/shakshuka-recipe/',
  // Pasta
  'https://www.recipetineats.com/carbonara/',
  'https://www.budgetbytes.com/spaghetti-with-vegetable-meat-sauce/',
  // Mains
  'https://www.recipetineats.com/oven-baked-chicken-and-rice/',
  'https://www.recipetineats.com/butter-chicken/',
  'https://www.recipetineats.com/thai-green-curry/',
  'https://www.recipetineats.com/baked-salmon/',
  'https://www.recipetineats.com/roast-chicken/',
